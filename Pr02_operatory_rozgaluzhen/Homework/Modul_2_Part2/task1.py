n = int(input('Please, input number of day of the week (1-7): '))
if n == 1:
    print ('Monday')
elif n == 2:
    print ('Tuesday')
elif n == 3:
    print ('Wednesday')
elif n == 4:
    print ('Thursday')
elif n == 5:
    print ('Friday')
elif n == 6:
    print ('Saturday')
else:
    print ('Sunday')
